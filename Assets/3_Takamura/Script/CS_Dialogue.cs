using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using TMPro;
using System.Text.RegularExpressions;

public class CS_Dialogue : MonoBehaviour
{
    [SerializeField, Header("名前テキストUI")]
    TextMeshProUGUI talkernameText;
    [SerializeField, Header("セリフテキストUI")]
    TextMeshProUGUI dialogueText;
    [SerializeField, Header("テキストファイル")]
    TextAsset file;
    //! @brief 読込用バッファ
    string pBuffer;
    //! @brief 改行で分割した1文
    string[] splitText;
    //! @brief 名前で分割した1文
    string[] nameText;
    //! @brief テキスト配列のインデックス
    int textIndex;

    [SerializeField, Header("次の文字を表示するまでの時間(秒)")]
    float delayDuration;
    [SerializeField, Header("1文表示完了後の待機時間(秒)")]
    float waitSecond;
    //! @brief コルーチン
    Coroutine showCoroutine;
    //! @brief 一文表示終了フラグ
    bool bFinishString;
    [SerializeField, Header("表示終了時のアイコンテキスト")]
    GameObject goFinish;
    [SerializeField, Header("自動送り機能")]
    bool bAuto;

    [SerializeField, Header("効果音")]
    AudioSource se;

    //! @brief 一度だけ処理を行うフラグ
    bool bOnce = false;
    //! @brief 表示処理が有効かどうか
    bool bEnable = false;
    public bool Benable
    {
        get { return bEnable; }
        set { bEnable = value; }
    }

    // Start is called before the first frame update
    void Start()
    {
        if (!bAuto)
        {
            waitSecond = 0.5f;
        }
        bEnable = false;
        bFinishString = true;
        goFinish.SetActive(false);
        LoadText();
        SplitString();
    }

    // Update is called once per frame
    void Update()
    {
        if (bEnable == false) return;
        bool tmp = bAuto ? (bFinishString == true) : ((Input.GetMouseButtonDown(0) || Input.GetKeyDown(KeyCode.Return)) && (bFinishString == true));
        if(bOnce == false)
        {
            tmp = true;
            bOnce = true;
        }
        if (tmp)
        {
            if(textIndex < splitText.Length - 1)
            {
                if (!string.IsNullOrEmpty(splitText[textIndex]))
                {
                    //! 名前判定
                    int indexName = splitText[textIndex].IndexOf("/");
                    int indexPause = splitText[textIndex].IndexOf("（");
                    int indexSe = splitText[textIndex].IndexOf("#");
                    if (indexName != -1)
                    {
                        //! 名前更新
                        talkernameText.text = splitText[textIndex].Replace("/","");

                        //! セリフ表示
                        dialogueText.text = splitText[++textIndex];
                        Show();
                        bFinishString = false;
                        textIndex++;
                    }
                    else if(indexPause != -1)
                    {
                        //! 名前非表示
                        talkernameText.text = " ";
                        //! 括弧付きの文字列表示
                        dialogueText.text = " ";
                        Show();
                        bFinishString = false;
                        textIndex++;
                        bEnable = false;
                    }
                    else if(indexSe != -1)
                    {
                        dialogueText.text = splitText[textIndex].Replace("#", "");
                        if (se != null) se.Play();
                        //Show();
                        textIndex++;
                    }
                    else
                    {
                        //! セリフのみ更新
                        dialogueText.text = splitText[textIndex];
                        Show();
                        bFinishString = false;
                        textIndex++;
                    }
                }
            }
            else
            {
                this.gameObject.SetActive(false);
            }

        }
        goFinish.SetActive(bFinishString);
    }

    //! @brief テキストファイルを読み込む
    bool LoadText()
    {
        pBuffer = file.text;
        if(pBuffer == null)
        {
            Debug.Log("テキストファイルが読み込めません。");
            return false;
        }
        return true;
    }
    //! @brief 改行で分割する
    void SplitString()
    {
        splitText = Regex.Split(pBuffer, @"\r?\n\r?\n"); ;
    }

    //! @brief 文字送り演出表示
    void Show()
    {
        if(showCoroutine != null)
        {
            StopCoroutine(showCoroutine);
        }
        //! 1文字ずつ表示するコルーチン実行
        showCoroutine = StartCoroutine(ShowCoroutine());
    }

    IEnumerator ShowCoroutine()
    {
        //! 待機用コルーチン
        var delay = new WaitForSeconds(delayDuration);

        int length = dialogueText.text.Length;

        //! 1文字ずつ表示
        for(int i = 0; i <= length; i++)
        {
            dialogueText.maxVisibleCharacters = i;
            //! 一定時間待機
            yield return delay;
        }

        yield return new WaitForSeconds(waitSecond);
        
        //! 演出が終わったらすべての文字を表示
        dialogueText.maxVisibleCharacters = length;

        bFinishString = true;

        showCoroutine = null;
    }
}
